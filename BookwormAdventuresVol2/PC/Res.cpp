bool Sexy::ExtractResourcesByName(ResourceManager *theManager, const char *theName)
{
	if (strcmp(theName,"CommonGame")==0) return ExtractCommonGameResources(theManager);
	if (strcmp(theName,"Credits")==0) return ExtractCreditsResources(theManager);
	if (strcmp(theName,"HallOfFame")==0) return ExtractHallOfFameResources(theManager);
	if (strcmp(theName,"Init")==0) return ExtractInitResources(theManager);
	if (strcmp(theName,"MiniGameCommon")==0) return ExtractMiniGameCommonResources(theManager);
	if (strcmp(theName,"MiniGameMenu")==0) return ExtractMiniGameMenuResources(theManager);
	if (strcmp(theName,"StatsBook")==0) return ExtractStatsBookResources(theManager);
	if (strcmp(theName,"TitleAndMM")==0) return ExtractTitleAndMMResources(theManager);
	if (strcmp(theName,"UpsellTrailer")==0) return ExtractUpsellTrailerResources(theManager);
	return false;
}

Sexy::ResourceId Sexy::GetIdByStringId(const char *theStringId)
{
	typedef std::map<std::string,int> MyMap;
	static MyMap aMap;
	if(aMap.empty())
	{
		for(int i=0; i<RESOURCE_ID_MAX; i++)
			aMap[GetStringIdById(i)] = i;
	}

	MyMap::iterator anItr = aMap.find(theStringId);
	if (anItr == aMap.end())
		return RESOURCE_ID_MAX;
	else
		return (ResourceId) anItr->second;
}

